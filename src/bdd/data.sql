CREATE DATABASE IF NOT EXISTS kasa CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE kasa;

-- ------------------------------------------------------------
-- Table : properties
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS properties (
  id          VARCHAR(10)   NOT NULL PRIMARY KEY,
  title       VARCHAR(255)  NOT NULL,
  cover       TEXT          NOT NULL,
  description TEXT          NOT NULL,
  host_name   VARCHAR(255)  NOT NULL,
  host_picture TEXT         NOT NULL,
  rating      TINYINT UNSIGNED NOT NULL,
  location    VARCHAR(255)  NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- Table : pictures
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS pictures (
  id          INT UNSIGNED  NOT NULL AUTO_INCREMENT PRIMARY KEY,
  property_id VARCHAR(10)   NOT NULL,
  url         TEXT          NOT NULL,
  CONSTRAINT fk_pictures_property FOREIGN KEY (property_id) REFERENCES properties(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- Table : equipments
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS equipments (
  id          INT UNSIGNED  NOT NULL AUTO_INCREMENT PRIMARY KEY,
  property_id VARCHAR(10)   NOT NULL,
  label       VARCHAR(255)  NOT NULL,
  CONSTRAINT fk_equipments_property FOREIGN KEY (property_id) REFERENCES properties(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- Table : tags
-- ------------------------------------------------------------
CREATE TABLE IF NOT exists tags (
  id          INT UNSIGNED  NOT NULL AUTO_INCREMENT PRIMARY KEY,
  property_id VARCHAR(10)   NOT NULL,
  label       VARCHAR(255)  NOT NULL,
  CONSTRAINT fk_tags_property FOREIGN KEY (property_id) REFERENCES properties(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

